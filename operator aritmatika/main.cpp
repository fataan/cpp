#include <iostream>

using namespace std;

int main(){

	int a = 3;
	float b = 2;

	float result;

	// perkalian
	result = a + b;
	cout << result << endl;

	// pengurangan
	result = a - b;
	cout << result << endl;

	// pengalian
	result = a * b;
	cout << result << endl;

	// pembagian 
	result = a / b;
	cout << result << endl; // jika mau desimal a atau b dan result harus menggunakan float

	// modulus
	// result = a % b;
	// cout << result << endl; // modulus tidak bisa menggunakan decimal

	return 0;
} 
